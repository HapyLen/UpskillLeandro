function createCourseHTML(cursos) {
    let html="";

// Função para criar o HTML dos cursos com o loop correto de 3 em 3 para adicionar o row
for (let i = 0; i < cursos.length; i += 3) {
    const linhaCursos = cursos.slice(i, i + 3);
    html += '<div class="row">'; // Inicia uma nova linha
    linhaCursos.forEach(curso => {
        const favClass = curso.fav ? 'favorito' : '';
        html += `
            <div class="four columns">
                <div class="card">
                    <img src="img/heart2.png" class="coracao ${favClass}" data-id="${curso.ID}" onclick="favorite(event)">
                    <img src="${curso.Imagem[0]}" class="imagen-curso u-full-width" alt="${curso.Nome[0]}">
                    <div class="info-card">
                        <h4>${curso.Nome[0]}</h4>
                        <p>${curso.Autor[0]}</p>
                        <img src="img/estrelas.png" alt="Estrelas">
                        <p class="preco">${curso.Preco[0]} <span class="u-pull-right">${curso.promocao[0]}</span></p>
                        <button class="u-full-width button-primary button input adicionar-carrinho" 
                            data-id="${curso.ID}" 
                            data-nome="${curso.Nome[0]}" 
                            data-preco="${curso.Preco[0]}">Adicionar ao Carrinho</button>
                    </div>
                </div>
            </div>`;
    });
    html += '</div>'; // meti isto em função porque reutilizei bastantes vezes no app.js, isto melhora a qualidade e facilita a leitura também
}
return html;
}
